        <h2>什么是融资租赁
            <span class="span1">What is financial leasing</span>
        </h2>
        <div class="left">
            <b style="text-align: center;">
                名词解释</b>
            <p>融资租赁（Financial Leasing）又称设备租赁（Equipment Leasing）或现代租赁(Modern Leasing)，是指实质上转移与资产所有权有关的全部或绝大部分风险和报酬的租赁。资产的所有权最终可以转移，也可以不转移。</p>
        </div>
        <div class="right">
            <b style="text-align: center;">
                经营定位</b>
            <p>融资租赁公司的经营定位应定位：服务于金融、贸易、产业的资产管理机构。是服务贸易中更多技术服务含量的服务。虽然也涉及资金，主线是为出资人服务，而不是把自有资金全部占压、套牢在项目中的风险投资业务。因此租赁公司既不是银行，也不是贷款公司，更不是投资公司，而是一个知识服务性的技术公司。</p>
        </div>
